   public int getAssociationType(){
       
       int radioID = 0;
       
       if (NoAssociationRadio.isSelected() == true){
           radioID = 1;
       } else if (LinearAssociationRadio.isSelected() == true){
           radioID = 2;
       } else if (QuadraticAssociationRadio.isSelected() == true){
           radioID = 3;
       }
     
       return radioID;
   }
   
   
